    public void main() {

        MouseListener mouseListener = new MouseAdapter() {
            public void mouseClicked(MouseEvent mouseEvent) {
                JList<String> theList = (JList) mouseEvent.getSource();
                if (mouseEvent.getClickCount() == 2) {
                    int index = theList.locationToIndex(mouseEvent.getPoint());
                    if (index >= 0) {
                        Object o = theList.getModel().getElementAt(index);
                        String[] parts = o.toString().split("°");
                        String opleiding_Id = parts[1];

                        System.out.println(opleiding_Id);
                        showOpleidingInfo(opleiding_Id);

                    }
                }
            }
        };
        list_opleiding.addMouseListener(mouseListener);
        txt_opleiding.hide();
        txt_locatie.hide();
        txt_datum.hide();
        txt_ID.hide();

        jLabel1.hide();
        jLabel4.hide();

        jLabel2.hide();
        jLabel3.hide();
        btn_wijzigen.hide();
        btn_verwijderen.hide();
        btn_surveyToevoegen.hide();
        btn_surveyTonen.hide();

    }

    public void showOpleidingInfo(String opleidingId) {
        PreparedStatement ps;
        ResultSet rs;

        String query = "SELECT * FROM `opleiding` WHERE id =  ?";

        try {
            ps = MyConnection.getConnection().prepareStatement(query);
            ps.setString(1, opleidingId);
            rs = ps.executeQuery();

            while (rs.next()) {
                txt_ID.setText(rs.getString("id"));
                txt_opleiding.setText(rs.getString("opleiding"));
                txt_datum.setText(rs.getString("datum"));
                txt_locatie.setText(rs.getString("location"));

                txt_opleiding.setVisible(true);
                txt_locatie.setVisible(true);
                txt_datum.setVisible(true);
                txt_ID.setVisible(true);

                jLabel1.setVisible(true);
                jLabel4.setVisible(true);

                jLabel2.setVisible(true);
                jLabel3.setVisible(true);
                btn_wijzigen.setVisible(true);
                btn_verwijderen.setVisible(true);
                btn_surveyToevoegen.setVisible(true);
                btn_surveyTonen.setVisible(true);

                // this.dispose();
            }
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(Login.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }

