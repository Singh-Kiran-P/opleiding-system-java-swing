    public void wijzig_database() throws Exception {
        try {
            // This will load the MySQL driver, each DB has its own driver
            Class.forName("com.mysql.jdbc.Driver");
            // Setup the connection with the DB
            connect = DriverManager
                    .getConnection("jdbc:mysql://localhost/stagedb?"
                            + "user=root&password=");

            // PreparedStatements can use variables and are more efficient
            preparedStatement = connect
                    .prepareStatement("UPDATE `opleiding` SET `opleiding` = ?, `location` = ?, `datum` = ? WHERE `opleiding`.`id` = ?");
            // "myuser, webpage, datum, summary, COMMENTS from feedback.comments");
            // Parameters start with 1
            preparedStatement.setString(1, txt_opleiding.getText());
            preparedStatement.setString(2, txt_locatie.getText());

            preparedStatement.setString(4, txt_ID.getText());

            String sDate1 = txt_datum.getText();

            preparedStatement.setDate(3, java.sql.Date.valueOf(sDate1));

            preparedStatement.executeUpdate();
            JOptionPane.showMessageDialog(null, "Opleiding wijzigt");
            listInladen();

        } catch (Exception e) {
            throw e;
        }

    }

    public void verwijder_database() throws Exception {
        try {
            // This will load the MySQL driver, each DB has its own driver
            Class.forName("com.mysql.jdbc.Driver");
            // Setup the connection with the DB
            connect = DriverManager
                    .getConnection("jdbc:mysql://localhost/stagedb?"
                            + "user=root&password=");

            // PreparedStatements can use variables and are more efficient
            preparedStatement = connect
                    .prepareStatement("DELETE FROM `opleiding` WHERE `opleiding`.`id` = ?");
            // "myuser, webpage, datum, summary, COMMENTS from feedback.comments");
            // Parameters start with 1
            preparedStatement.setString(1, txt_ID.getText());

            preparedStatement.executeUpdate();
            JOptionPane.showMessageDialog(null, "Opleiding verwijderd");
            txt_ID.setText("");
            txt_opleiding.setText("");
            txt_datum.setText("");
            txt_locatie.setText("");
            txt_opleiding.hide();
            txt_locatie.hide();
            txt_datum.hide();
            txt_ID.hide();

            jLabel1.hide();
            jLabel4.hide();

            jLabel2.hide();
            jLabel3.hide();
            btn_wijzigen.hide();
            btn_verwijderen.hide();
            btn_surveyToevoegen.hide();
            btn_surveyTonen.hide();

            listInladen();

        } catch (Exception e) {
            throw e;
        }

    }


